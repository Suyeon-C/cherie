package com.dto;

/* 판매자 회원 */
public class Seller {

	private String sellEmail;
	
	private String sellPassword;
	
	private String sellName;
	
	private String sellPhone;
	
	private String sellBirth;
	
	private String sellZipcode;
	
	private String sellAddress;
	
	private String sellDaddress;
	
	private int sellRegNumber;
	

	public String getSellEmail() {
		return sellEmail;
	}

	public void setSellEmail(String sellEmail) {
		this.sellEmail = sellEmail;
	}

	public String getSellPassword() {
		return sellPassword;
	}

	public void setSellPassword(String sellPassword) {
		this.sellPassword = sellPassword;
	}

	public String getSellName() {
		return sellName;
	}

	public void setSellName(String sellName) {
		this.sellName = sellName;
	}

	public String getSellPhone() {
		return sellPhone;
	}

	public void setSellPhone(String sellPhone) {
		this.sellPhone = sellPhone;
	}

	public String getSellBirth() {
		return sellBirth;
	}

	public void setSellBirth(String sellBirth) {
		this.sellBirth = sellBirth;
	}

	public String getSellZipcode() {
		return sellZipcode;
	}

	public void setSellZipcode(String sellZipcode) {
		this.sellZipcode = sellZipcode;
	}

	public String getSellAddress() {
		return sellAddress;
	}

	public void setSellAddress(String sellAddress) {
		this.sellAddress = sellAddress;
	}

	public String getSellDaddress() {
		return sellDaddress;
	}

	public void setSellDaddress(String sellDaddress) {
		this.sellDaddress = sellDaddress;
	}

	public int getSellRegNumber() {
		return sellRegNumber;
	}

	public void setSellRegNumber(int sellRegNumber) {
		this.sellRegNumber = sellRegNumber;
	}


}
